class escapeSequence{

	public static void main(String args[]){
		
		System.out.println("Manuel\nRajesh");//to have new line(\n)
		System.out.println("Manuel\tRajesh");//to have a tab in between (\t)
		System.out.println("Manuel\'Rajesh");//to have a single quote in between(\')
		System.out.println("Manuel\"Rajesh");//to have a double quote in between(\")
		System.out.println("Manuel\\Rajesh");//to have a backslash in between(\\)

}
} 